package com.sky.bill.beans;

public class Movie {

	private String title;
	private Double cost;
	
	public Movie() {
	}
	
	public String getTitle() {
		return title;
	}
	
	public void setTitle(String title) {
		this.title = title;
	}
	
	public Double getCost() {
		return cost;
	}
	
	public void setCost(Double cost) {
		this.cost = cost;
	}
}
